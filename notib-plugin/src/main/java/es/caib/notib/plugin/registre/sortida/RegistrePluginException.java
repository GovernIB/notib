package es.caib.notib.plugin.registre.sortida;

public class RegistrePluginException extends Exception {
	
	private static final long serialVersionUID = 1L;
	
	public RegistrePluginException() { super(); }
	public RegistrePluginException(String message) { super(message); }
	public RegistrePluginException(String message, Throwable cause) { super(message, cause); }
	public RegistrePluginException(Throwable cause) { super(cause); }

}